package first;


public class PrimativeDataTypeExam {

	public static void main(String[] args) {
		boolean isFun = true;
		System.out.println(isFun);
		
		char c = 'a';  //char형은 작은 따옴표, 문자하나만을 표현가능
		int x = 59;
		double y = 1;
		long bing = 34589978l;  //long타입 값 뒤에 소문자 l 또는 대문자 L을 넣어야 됌 
		float f = 32.4F;        //float타입 값 뒤에 소문자 f 또는 대문자 F를 넣어야 됌
		System.out.println(f);
		
	}

}
